public class RainSizeUtil {

    private int rainNum;
    private float rainMaxSize;
    private float rainMinSize;
    private int rainSpeed;

    public float getRainMaxSize() {
        return rainMaxSize;
    }

    public void setRainMaxSize(float rainMaxSize) {
        this.rainMaxSize = rainMaxSize;
    }

    public float getRainMinSize() {
        return rainMinSize;
    }

    public void setRainMinSize(float rainMinSize) {
        this.rainMinSize = rainMinSize;
    }

    public int getRainNum() {
        return rainNum;
    }

    public void setRainNum(int rainNum) {
        this.rainNum = rainNum;
    }

    public int getRainSpeed() {
        return rainSpeed;
    }

    public void setRainSpeed(int rainSpeed) {
        this.rainSpeed = rainSpeed;
    }

}
